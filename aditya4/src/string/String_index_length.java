package string;

public class String_index_length {

	public static void main(String[] args) {
		String str1="this is a java";
		System.out.print(str1.length()+" ");
		System.out.println(str1.charAt(3));
		String str2=" hi i am here" ;
		System.out.print(str2.length()+ " ");
		System.out.println(str2.charAt(2));
		
		String str3="Where are you " ;
		System.out.print(str3.length()+ " ");
		System.out.println(str3.charAt(0));
		String str4="this is demo";
		System.out.print(str4.length()+" ");
		System.out.println(str4.charAt(5));
		String str5="core java";
		System.out.print(str5.length()+" ");
		System.out.println(str5.charAt(6));

	}

}
