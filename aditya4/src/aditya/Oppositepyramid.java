package aditya;
import java.util.*;
public class Oppositepyramid {

	public static void main(String[] args) 
	{
	Scanner ad=new Scanner (System.in);
	int m=ad.nextInt();
	for (int i=m; i>=1; i--)
	     {
		
			for (int j=1; j<=i; j++) 
		
		{
			System.out.print("*");
		
	    }      System.out.println();
	    }

        }
}